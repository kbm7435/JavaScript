const kelvin = 293; 
// variable kelvin is set to 293

let celsius; 
// variable celsius 

celsius = kelvin - 273;
//calculated celsius 


console.log(celsius);
// printed out celsius 

let fahrenheit;
fahrenheit = celsius*(9/5)+32 ;
console.log(fahrenheit);
Math.floor(fahrenheit); 
console.log(`The temperature is ${fahrenheit} degrees Fahrenheit`);
// using string interpolation to print out the temperature 

